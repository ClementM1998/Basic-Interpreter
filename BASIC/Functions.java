
public class Functions {
    /*

    Fungsi-Fungsi yang Disokong dalam Dartmouth BASIC

    1. Fungsi Matematik
    ABS(X) → Mengembalikan nilai mutlak bagi X.
    Contoh: PRINT ABS(-10) → Output: 10

    INT(X) → Mengembalikan bahagian bulat bagi X dengan pembulatan ke bawah.
    Contoh: PRINT INT(4.9) → Output: 4

    SQR(X) → Mengembalikan punca kuasa dua bagi X.
    Contoh: PRINT SQR(9) → Output: 3

    RND(X) → Mengembalikan nombor rawak antara 0 dan 1.
    Contoh: PRINT RND(1)

    2. Fungsi Trigonometri
    SIN(X), COS(X), TAN(X) → Mengira nilai sinus, kosinus, dan tangen bagi sudut dalam radian.
    Contoh: PRINT SIN(3.14/2) → Output: 1

    ATN(X) → Mengembalikan arctangent bagi X.

    3. Fungsi String
    CHR$(X) → Mengembalikan watak ASCII bagi nombor X.
    Contoh: PRINT CHR$(65) → Output: A

    ASC(X$) → Mengembalikan kod ASCII bagi aksara pertama dalam X$.
    Contoh: PRINT ASC("A") → Output: 65

    LEFT$(X$, N) → Mengambil N aksara pertama daripada X$.
    Contoh: PRINT LEFT$("HELLO",2) → Output: HE

    RIGHT$(X$, N) → Mengambil N aksara terakhir daripada X$.
    Contoh: PRINT RIGHT$("HELLO",2) → Output: LO

    MID$(X$, Start, N) → Mengambil N aksara bermula dari kedudukan Start dalam X$.
    Contoh: PRINT MID$("HELLO",2,3) → Output: ELL

    4. Fungsi Input dan Output
    PRINT X → Mencetak nilai X ke skrin.

    INPUT X → Menerima input daripada pengguna dan menyimpannya dalam pembolehubah X.

    TAB(N) → Memindahkan kursor ke kedudukan N.
    Contoh: PRINT TAB(10); "HELLO"

    SPC(N) → Menyisipkan N ruang kosong dalam cetakan.

    5. Fungsi Lain
    TIME → Mengembalikan masa semasa dalam saat sejak program dimulakan.
    RND(X) → Menghasilkan nombor rawak antara 0 dan 1.

     */
}
